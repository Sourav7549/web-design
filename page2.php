                <b><h3>Benifits of Dialectical Behavior Therapy (DBT)?</h3></b>
                <br/>
            </div>

                <br style="list-style-type:circle;">
                <b><li>Acceptance and change:</b> You’ll learn strategies to accept and tolerate your life circumstances, emotions, and yourself. You will also develop skills that can help you make positive changes in your behaviors and interactions with others.</li><br/>
                
                    <b><li>Behavioral:</b> You'll learn to analyze problems or destructive behavior patterns and replace them with more healthy and effective ones.</li><br/>
                    
                    <b><li>Cognitive:</b> You'll focus on changing thoughts and beliefs that are not effective or helpful.  
